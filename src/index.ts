/**
* Adds two numbers
* @param a Number one
* @param a Number two
* @returns Result
*/
export const add = (a: number, b:number): number => a + b;

/**
* Substracts two numbers
* @param a Number one
* @param a Number two
* @returns Result
*/
export const substract = (a: number, b: number): number => a - b;


/**
* Multiplies two numbers
* @param a Number one
* @param a Number two
* @returns Result
*/
export const mutiply = (a: number, b: number): number => a * b;